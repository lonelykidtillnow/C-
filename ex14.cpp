#include<bits/stdc++.h>
using namespace std;
int main(){
  int n;
  cin>>n;   //1957
  
  cout<<"Total No.of Notes"<<endl;
  
  cout<<"2000 : "<<n/2000<<endl;  //1957/2000=0
  n=n%2000;   //1957%2000=1957
  
  cout<<"1000 : "<<n/1000<<endl;  //1957/1000=1
  n=n%1000;   //1957%1000=957
  
  cout<<"500 : "<<n/500<<endl;    //957/500=1
  n=n%500;    //957%500=457
  
  cout<<"200 : "<<n/200<<endl;    //457/200=2
  n=n%200;    //457%200=57
  
  cout<<"100 : "<<n/100<<endl;    //100/57=0
  n=n%100;    //57%100=57
  
  cout<<"50 : "<<n/50<<endl;    //57/50=1
  n=n%50;   //57%50=7
  
  cout<<"20 : "<<n/20<<endl;    //7/20=0
  n=n%20;   //7%20=/7
  
  cout<<"10 : "<<n/10<<endl;    //7/10=0
  n=n%10;   //7%10=7
  
  cout<<"5 : "<<n/5<<endl;      //7/5=1
  n=n%5;    //7%5=2
  
  cout<<"2 : "<<n/2<<endl;    //2/2=1
  n=n%2;    //2%2=0
  
  cout<<"1 : "<<n/1<<endl;    //0/1=0
  n=n%1;
  
  return 0;
}